TuffleMath = {}

function TuffleMath.Sum(N1, N2)
    return N1 + N2
end

function TuffleMath.Sub(N1, N2)
    return N1 - N2
end

function TuffleMath.Multiply(N1, N2)
    return N1 * N2
end

function TuffleMath.Division(N1, N2)
    return N1 / N2
end

function TuffleMath.Power(N1, N2)
    return N1 ^ N2
end

function TuffleMath.Sqrt(N)
    return N % N
end

return TuffleMath